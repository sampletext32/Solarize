namespace UnityUnitConverters
{
    public abstract class UnityUnitAsBase
    {
        public abstract float FromMeters(float meters);

        public abstract float FromKilometers(float kiloMeters);

        public abstract float FromMegameters(float megaMeters);

        public abstract float FromGigameters(float gigaMeters);

        public abstract float FromAstronomicalUnits(float astronomicalUnits);

        public abstract float FromLightyears(float lightYears);
    }
}